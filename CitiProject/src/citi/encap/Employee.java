package citi.encap;
//POJO CLASS
public class Employee {
//variable
private int id;
private String name;

//receive the input /set//setter

public void setId(int id) {
	this.id = id;
}
public void setName(String name) {
	this.name = name;
}
//get /logic //getter
public int getId() {
	return id;
}
public String getName() {
	return name;
}

}
